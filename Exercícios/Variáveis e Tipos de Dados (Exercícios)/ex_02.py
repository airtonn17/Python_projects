"""
2. Faça um programa que peça para o usuário digitar três valores inteiro e imprima a soma deles.
"""

n1 = int(input('Digite o primeiro valor inteiro: '))
n2 = int(input('Digite o segundo valor inteiro: '))
n3 = int(input('Digite o terceiro valor inteiro: '))

soma = n1 + n2 + n3

print(f'A soma de {n1} + {n2} + {n3} é: {soma}')